    enum Condition {
        DAMAGED, OPERATIONAL, UNKNOWN;

        @Override
        public String toString() {
            if (this == DAMAGED) {
                return "#";
            } else if (this == OPERATIONAL) {
                return ".";
            } else {
                return "?";
            }
        }
    }

