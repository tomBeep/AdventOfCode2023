    private List<List<Condition>> getAllCombinations(List<List<Condition>> currentCombinations, int indexToCheck,
                                                     List<Condition> initialList, List<Integer> brokenGroups,
                                                     long unknownsLeftInLine, long damagedInLine, long operationalInLine) {
        Iterator<List<Condition>> iterator = currentCombinations.iterator();
        while (iterator.hasNext()) {
            var combo = iterator.next();
            if (isInvalidCombo(combo, brokenGroups, unknownsLeftInLine, damagedInLine, operationalInLine)) {
                iterator.remove();
            }
        }
        if (currentCombinations.isEmpty()) {
            return Collections.emptyList();
        }

        if (indexToCheck >= initialList.size()) {
            return currentCombinations;
        }
        Condition conditionAtIndex = initialList.get(indexToCheck);
        if (conditionAtIndex != Condition.UNKNOWN) {
            List<List<Condition>> newCurrentCombinations = new ArrayList<>();
            for (List<Condition> combo : currentCombinations) {
                List<Condition> newList = new ArrayList<>(combo);
                newList.add(conditionAtIndex);
                newCurrentCombinations.add(newList);
            }
            long newDamagedInLine = conditionAtIndex == Condition.DAMAGED ? damagedInLine - 1 : damagedInLine;
            long newOperationInLine = conditionAtIndex == Condition.OPERATIONAL ? operationalInLine - 1 : operationalInLine;
            return getAllCombinations(newCurrentCombinations, indexToCheck + 1, initialList, brokenGroups, unknownsLeftInLine, newDamagedInLine, newOperationInLine);
        } else {
            // condition at index must be unknown...
            List<List<Condition>> listsWithDamaged = new ArrayList<>();
            List<List<Condition>> listsWithOperational = new ArrayList<>();
            for (List<Condition> combo : currentCombinations) {
                List<Condition> combo1 = new ArrayList<>(combo);
                combo1.add(Condition.DAMAGED);
                List<Condition> combo2 = new ArrayList<>(combo);
                combo2.add(Condition.OPERATIONAL);
                listsWithDamaged.add(combo1);
                listsWithOperational.add(combo2);
            }
            List<List<Condition>> combos = getAllCombinations(listsWithDamaged, indexToCheck + 1, initialList, brokenGroups, unknownsLeftInLine - 1, damagedInLine, operationalInLine);
            List<List<Condition>> combos2 = getAllCombinations(listsWithOperational, indexToCheck + 1, initialList, brokenGroups, unknownsLeftInLine - 1, damagedInLine, operationalInLine);
            List<List<Condition>> combined = new ArrayList<>();
            combined.addAll(combos);
            combined.addAll(combos2);
            return combined;
        }

    }

    private boolean isInvalidCombo(List<Condition> combo, List<Integer> brokenGroups, long unknownsLeftInLine, long damagedLeftInLine, long operationalLeftInLine) {
        int brokenInCurrentGroup = 0;
        int indexOfBrokenGroups = 0;
        int totalDamaged = 0;
        int totalExpectedDamaged = brokenGroups.stream().mapToInt(i -> i).sum();
        int requiredOperationalBreaks = brokenGroups.size() - 1;

        String requiredNextCharacter = "";

        for (int i = 0; i < combo.size(); i++) {
            Condition conditionAtIndex = combo.get(i);
            if (conditionAtIndex == Condition.DAMAGED) {
                brokenInCurrentGroup++;
                totalDamaged++;
                if (indexOfBrokenGroups >= brokenGroups.size() || brokenInCurrentGroup > brokenGroups.get(indexOfBrokenGroups)) {
                    return true;
                }
            } else if (conditionAtIndex == Condition.OPERATIONAL) {
                if (brokenInCurrentGroup > 0) {
                    if (brokenInCurrentGroup != brokenGroups.get(indexOfBrokenGroups)) {
                        return true;
                    }
                    requiredOperationalBreaks--;
                    brokenInCurrentGroup = 0;
                    indexOfBrokenGroups++;
                }
            } else {
                throw new Error();
            }
        }
        if (totalDamaged + unknownsLeftInLine + damagedLeftInLine < totalExpectedDamaged) {
            return true;
        }
        if (operationalLeftInLine + unknownsLeftInLine < requiredOperationalBreaks) {
            return true;
        }
        if (brokenInCurrentGroup > 0) {
            if (brokenInCurrentGroup == brokenGroups.get(indexOfBrokenGroups)) {
                requiredNextCharacter = ".";
            } else {
                requiredNextCharacter = "#";
            }
        }
        return false;
    }

