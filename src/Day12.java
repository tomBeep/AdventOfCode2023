    private Condition mapToCondition(char c) {
        if (c == '.') {
            return Condition.OPERATIONAL;
        } else if (c == '#') {
            return Condition.DAMAGED;
        } else if (c == '?') {
            return Condition.UNKNOWN;
        } else {
            throw new Error("unknown char: " + c);
        }
    }

